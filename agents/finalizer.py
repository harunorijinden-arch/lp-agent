"""
ファイナライザーエージェント（Step7）
====================================
ディスカッション結果を反映した最終ワイヤーフレームを自動生成する。
参照資料なし（Step5のワイヤー + Step6のディスカッション結果のみ）。
"""

from agents.base import call_claude, console
from rich.panel import Panel

TOTAL_STEPS = 7
MAX_TOKENS = 32000

SYSTEM = (
    "あなたはLP設計のファイナライザー（最終仕上げ担当）です。\n\n"
    "【あなたの仕事】\n"
    "Step5で作成されたワイヤーフレーム提案を、\n"
    "Step6の4人ディスカッション（3ラウンド）の結論を反映して、\n"
    "最終版ワイヤーフレームとして完成させること。\n\n"
    "【ルール】\n"
    "- ディスカッションで「✅ 決まったこと」に挙がった改善は必ず反映する\n"
    "- ディスカッションで指摘された弱点・問題点を修正する\n"
    "- 本命案と対抗案のうち、ディスカッションで推奨された案を最終案として仕上げる\n"
    "  （必要なら両案を統合したハイブリッド版を作成する）\n"
    "- LP目的（Who/What定義の冒頭に記載）が達成できる設計になっているか最終確認する\n"
    "- 出力は「そのまま制作チームに渡せるレベル」のワイヤーフレームにする\n"
    "- 日本語で回答すること\n"
)


class FinalizerAgent:
    """最終ワイヤーフレーム生成エージェント"""

    def run(
        self,
        wireframe: str,
        discussion: str,
        discussion_summary: str,
        who_what: str,
    ) -> str:
        """Step7: ディスカッション結果を反映した最終ワイヤーフレーム"""
        console.print(Panel(
            f"[bold white on magenta]Step 7/{TOTAL_STEPS}: 最終ワイヤーフレーム生成[/bold white on magenta]\n"
            "ディスカッション結果を反映して最終版を作成中...\n"
            "[dim]🧠 ファイナライザーエージェントが担当[/dim]",
            border_style="magenta",
        ))

        prompt = (
            "## タスク\n\n"
            "以下の情報をすべて踏まえて、**最終版ワイヤーフレーム**を作成してください。\n\n"
            "---\n\n"
            "## Who / What 定義（LP目的を含む）\n\n"
            f"{who_what}\n\n"
            "---\n\n"
            "## Step5のワイヤーフレーム提案（修正前）\n\n"
            f"{wireframe}\n\n"
            "---\n\n"
            "## ディスカッション結論サマリー\n\n"
            f"{discussion_summary}\n\n"
            "---\n\n"
            "## 出力フォーマット（厳守）\n\n"
            "```\n"
            "# 最終ワイヤーフレーム\n\n"
            "## 採用案: （本命案 / 対抗案 / ハイブリッド）\n"
            "## 採用したLP型: （問題解決型 / 欲求充足型 / 潜在的需要型）\n"
            "## 採用理由: \n\n"
            "## ビッグアイデア\n"
            "★「                    」\n\n"
            "## ヘッドコピー（最終版・3案）\n"
            "1. \n"
            "2. \n"
            "3. \n\n"
            "## LP全体レイアウト概要図\n\n"
            "ASCIIアートでLPの全体構成を視覚的に示してください。\n"
            "各セクションの画面占有率（スマホ1画面 = 100vh基準）も記載。\n"
            "制作チームがこの図だけでLP全体の流れとボリューム感を把握できるように。\n\n"
            "例:\n"
            "┌─────────────────────────────┐\n"
            "│   ファーストビュー (100vh)    │ ← キャッチ + CTA\n"
            "├─────────────────────────────┤\n"
            "│   悩み共感 (60vh)            │ ← 問題提起\n"
            "├─────────────────────────────┤\n"
            "│   解決策 (80vh)              │ ← サービス紹介 + CTA\n"
            "├─────────────────────────────┤\n"
            "│   ...                        │\n"
            "└─────────────────────────────┘\n\n"
            "## LP構成（全セクション）\n\n"
            "各セクション:\n"
            "■ セクション{番号}: {セクション名}\n"
            "【目的】\n"
            "【画面占有率】（例: 100vh, 60vh等。スマホ基準）\n"
            "【レイアウト】（左右配置、カード型、全幅画像等の指示）\n"
            "【見出し案】（最終版）\n"
            "【掲載内容】（具体的に）\n"
            "【コピー方針】\n"
            "【CTA配置】\n"
            "【ディスカッションで改善した点】← どの議論を反映したか明記\n\n"
            "## CTA設計まとめ\n"
            "- メインCTA文言: \n"
            "- CTA配置回数: \n"
            "- CTA周辺のマイクロコピー: \n\n"
            "## アド・スルー設計（Meta広告主軸）\n"
            "- Meta広告クリエイティブとの世界観一貫性: \n"
            "- Meta広告経由の着地体験: \n"
            "- 受動的ユーザーを引き込むファーストビューの工夫: \n\n"
            "## LP目的の達成チェック\n"
            "- LP目的: \n"
            "- この最終ワイヤーフレームで達成できる理由: \n"
            "- 残るリスク・注意点: \n"
            "```\n"
        )

        return call_claude(SYSTEM, prompt, use_web_search=False, max_tokens=MAX_TOKENS)
